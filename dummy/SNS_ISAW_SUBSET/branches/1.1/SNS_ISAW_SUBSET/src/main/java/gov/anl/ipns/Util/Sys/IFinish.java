package gov.anl.ipns.Util.Sys;



/**
  *  Classes that implement this interface contain a finish method.  If the 
  *  class is an instance of a Window, it should dispose of the window and 
  *  ALSO finalize the window
  */

public interface IFinish{

  /**
    *  If the class that implements this interface is a Window, it should 
    *  dispose and finalize the window
    */ 
  public void finish();
}
